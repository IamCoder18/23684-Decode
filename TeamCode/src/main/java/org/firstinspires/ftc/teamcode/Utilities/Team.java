package org.firstinspires.ftc.teamcode.Utilities;

/**
 * Enum representing the team color for FTC competition.
 * <p>
 * Determines which side of the field the robot is competing on:
 * <ul>
 *   <li>BLUE - Blue alliance side</li>
 *   <li>RED - Red alliance side</li>
 *   <li>UNKNOWN - Team color not determined</li>
 * </ul>
 */
public enum Team {
	/**
	 * Blue alliance team
	 */
	BLUE,
	/**
	 * Red alliance team
	 */
	RED,
	/**
	 * Team color unknown or not set
	 */
	UNKNOWN
}
